function isValidIP(str) {
  // 膜拜一下神一样的正则
  // /^(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/

  // 普通写法：
  // 先校验四位数字
  if (!/^(?:\d{1,3}\.){3}\d{1,3}$/.test(str)) return false
  // 再判断每一位有没有超过 255
  return str.split('.').every(curr => +curr <= 255)
}
